/**
 * Нормализация поисковых запросов и синонимы для текстового поиска по каталогу.
 *
 * Цель — устойчивость к типичным «опечаткам» в русскоязычном поиске:
 *   • ё/е считаем одинаковыми;
 *   • латинские буквы-двойники (раскладка не переключена) сводим к кириллице —
 *     это самая частая причина «поиск ничего не нашёл» на RU-клавиатуре;
 *   • регистр и лишние пробелы убираем.
 *
 * ВАЖНО: строка FOLD_FROM/FOLD_TO продублирована в SQL (translate(...)) в
 * text-search route и в миграции scripts/enable-fuzzy-search.mjs. Эти три места
 * должны совпадать символ-в-символ, иначе функциональный индекс не будет
 * использоваться и нормализация запроса разойдётся с нормализацией колонки.
 */

// ё + латинские двойники → кириллица. Порядок символов в FROM/TO соответствует.
export const FOLD_FROM = "ёabcehkmoptxy";
export const FOLD_TO = "еавсенкмортху";

const FOLD_MAP: Record<string, string> = {};
for (let i = 0; i < FOLD_FROM.length; i++) {
  FOLD_MAP[FOLD_FROM[i]] = FOLD_TO[i];
}

/** Нормализация для сопоставления с НАЗВАНИЕМ (кириллица): регистр + ё→е + двойники. */
export function normalizeName(input: string): string {
  const lower = (input || "").toLowerCase();
  let out = "";
  for (const ch of lower) out += FOLD_MAP[ch] ?? ch;
  return out.replace(/\s+/g, " ").trim();
}

/** Нормализация для сопоставления с АРТИКУЛОМ: только регистр/пробелы.
 *  Двойники НЕ сводим — иначе латинские артикулы (AC123) сломаются. */
export function normalizeArticle(input: string): string {
  return (input || "").toLowerCase().replace(/\s+/g, " ").trim();
}

/** Разбивка на значимые токены (≥2 символов, максимум 5). */
export function tokenize(input: string): string[] {
  return input
    .split(/\s+/)
    .map((t) => t.trim())
    .filter((t) => t.length >= 2)
    .slice(0, 5);
}

/**
 * Синонимы и расшифровки автомобильных сокращений. Ключ — нормализованный
 * токен; значения — дополнительные подстроки названия, которые тоже считаем
 * совпадением для этого токена. Расширяет ПОИСК, никогда не сужает.
 */
const SYNONYMS: Record<string, string[]> = {
  гур: ["гидроусилител", "гидравлическ"],
  тосол: ["антифриз", "охлаждающ"],
  антифриз: ["тосол", "охлаждающ"],
  акпп: ["автоматическ", "акп"],
  мкпп: ["механическ", "мкп"],
  кпп: ["коробк передач", "трансмисси"],
  гбц: ["головк блок"],
  грм: ["газораспределит", "ремен грм", "цеп грм"],
  шрус: ["привод", "граната"],
  сайлентблок: ["сайлент", "втулк"],
  сайлент: ["сайлентблок"],
  амортизатор: ["стойк", "аморт"],
  свеча: ["свеч"],
  свечи: ["свеч"],
  дворники: ["щетк", "стеклоочистит"],
  дворник: ["щетк", "стеклоочистит"],
  колодки: ["колодк"],
  колодка: ["колодк"],
  фильтр: ["фильтр"],
  фильтра: ["фильтр"],
  лампа: ["ламп"],
  лампочка: ["ламп"],
  ремень: ["ремен"],
  подшипник: ["подшипник"],
  стойка: ["стойк", "амортизатор"],
};

/** Возвращает [сам токен, ...синонимы] для расширения поиска по названию. */
export function expandToken(token: string): string[] {
  const syn = SYNONYMS[token];
  return syn ? [token, ...syn] : [token];
}
