/** Все статьи, сгруппированные по категории (в порядке появления). */
export function getGuidesByCategory(): { category: string; guides: Guide[] }[] {
  const groups: { category: string; guides: Guide[] }[] = [];
  for (const g of GUIDES) {
    let group = groups.find((x) => x.category === g.category);
    if (!group) {
      group = { category: g.category, guides: [] };
      groups.push(group);
    }
    group.guides.push(g);
  }
  return groups;
}

export function getGuide(slug: string): Guide | undefined {
  return GUIDES.find((g) => g.slug === slug);
}

/** Готовая статья для категории каталога (по categorySlug), если есть. */
export function getGuideForCategory(categorySlug: string): Guide | undefined {
  return GUIDES.find(
    (g) => g.categorySlug === categorySlug && isGuideReady(g)
  );
}

/** У статьи есть готовый текст? (иначе — «Скоро».) */
export function isGuideReady(g: Guide): boolean {
  return g.blocks.length > 0;
}
