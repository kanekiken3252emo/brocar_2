/**
 * Политика возврата зависит от поставщика позиции.
 *
 * Товары «Берг» (Berg) идут под заказ у дистрибьютора и возврату надлежащего
 * качества НЕ подлежат. Все прочие поставщики — обычный возврат в течение
 * 10–14 дней.
 *
 * Признак берём по строке поставщика, как она сохранена в `cart_items.supplier`
 * (адаптер Берга пишет её как «Berg (склад)», см. lib/suppliers/berg.ts).
 * Клиенту саму строку не отдаём (в ней реальные имена складов) — наружу уходит
 * только булев `returnable`, посчитанный этой функцией на сервере.
 */

/** Окно возврата для возвратных товаров (надлежащего качества), в днях. */
export const RETURN_WINDOW_DAYS = "10–14";

/** true, если товар этого поставщика можно вернуть (не Берг). */
export function isReturnableSupplier(
  supplier: string | null | undefined
): boolean {
  // Неизвестный/легаси поставщик (строки без снимка) — считаем возвратным.
  if (!supplier) return true;
  const s = supplier.trim().toLowerCase();
  return !(s.startsWith("berg") || s.startsWith("берг"));
}
