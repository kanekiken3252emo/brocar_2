--
-- Зачем:
--   1) цена строки корзины больше НЕ «едет» при перезаписи общего products.ourPrice
--      (повторный add того же артикула, ночной импорт);
--   2) разные офферы одного артикула (разная цена/срок) живут ОТДЕЛЬНЫМИ строками,
--      а не схлопываются с задвоением количества (см. app/api/cart, /api/orders).
--
-- Колонка NULLABLE: легаси-строки (созданные до миграции) снимка не имеют —
-- для них цена берётся из products.ourPrice (фоллбэк в коде).
--
-- ВАЖНО: применить ДО деплоя нового кода. Новый код пишет/читает cart_items.price
-- безусловно; на непромигрированной БД /api/cart и /api/orders будут 500-ить.
